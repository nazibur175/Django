# Variable Examples
x = 10          # Integer
y = 5.5         # Float
name = "Alice"  # String
is_active = True # Boolean

print(x, y, name, is_active)

# Check data types
print(type(x))         
print(type(y))         
print(type(name))      
print(type(is_active)) 

# Arithmetic operations
a = 20
b = 3
print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division
print(a // b) # Floor division
print(a % b)  # Modulus
print(a ** b) # Exponentiation
